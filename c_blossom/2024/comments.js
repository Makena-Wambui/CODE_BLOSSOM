// Singe line comments are like this:
// Can precede the line of code

// This prints out "Hello, World!" to the console
console.log("Hello, World!");

// OR the comment can come after the line of code
console.log("Hello, World!"); // This prints out "Hello, World!" to the console

// Multi-line comments are like this:
/*
This is a multi-line comment.
It can span multiple lines.
*/

// You can also use multi line comment syntax in the middle of a line of code
console.log(/*This is ignored*/ "Hello, World!");
