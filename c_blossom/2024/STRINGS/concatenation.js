// When the addition operator is used on 2 strings, it appends the right string to the left string.

console.log("Hi" + "ya!"); // "Hiya!"
console.log("Hello" + " World"); // "Hello World"

// Concatenation is the process of appending one string to another

console.log("My" + " " + "son");

// We can also combine or chain strings to get one string
console.log("One" + ", " + "Two" + "," + " " + "Three" + "!");
