// Change the case using toUpperCase() and toLowerCase() methods

console.log("Productivity".toLowerCase()); // productivity; will change all characters to lowercase
console.log("Productivity".toUpperCase()); // PRODUCTIVITY; will change all characters to uppercase

// If you want to target a single character.
console.log("Productivity".at(-1).toUpperCase()); // at(-1) will extract the last character; Y. Then we change it to uppercase; Y
console.log("Productivity"[0].toLowerCase()); // p; will change the first character to lowercase
