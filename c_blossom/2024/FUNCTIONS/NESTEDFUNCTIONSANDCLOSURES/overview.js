// You can nest a function within another function

// The inner/nested function is private to its containing/outer function

// Nesting functions forms a closure.

// A closure is an expression, most commonly a function, that can have free variables, together with an environment that binds those variables.

// Like we said, a nested function is a closure.

// Hence, it can inherit the arguments and variables of its containing function.

// In other words, the inner function contains the scope of the outer function.

// In summary:
// The inner function can only be accessed in statements of the outer function.
// The inner function forms a closure: the inner function can use the arguments and variables of the outer function, while the outer function cannot use the arguments and variables of the inner function.
