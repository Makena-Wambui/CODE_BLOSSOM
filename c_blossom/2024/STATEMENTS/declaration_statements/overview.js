// Introduce variables, functions or classes into a program.

// Start with a JS Keyword like let, const, var, function, class, etc.
// Followed by a name for the variable, function or class, also called an identifier.
// Identifiers are case-sensitive.

// Variables
let x = 5;
let y = 6;
let z = x + y;

// Functions
function myFunction(a, b) {
  return a * b;
}

// Classes
class Car {
  constructor(brand) {
    this.carname = brand;
  }
}
